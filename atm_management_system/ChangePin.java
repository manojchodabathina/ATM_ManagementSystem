    private void ChangeBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ChangeBtnMouseClicked
        if (NewPinTxt.getText().isEmpty() || ConfirmTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter and Confirm The Pin");
        } else if (!NewPinTxt.getText().equals(ConfirmTxt.getText())) {
            JOptionPane.showMessageDialog(this, "New PIN And Confirm PIN Are Not Same");
        } else {
            try {
                String Query = "Update AccountTable set Pin = ? where AccNo = ?";
                Con = DriverManager.getConnection("jdbc:mysql://localhost:3308/atm_management_system", "root", "root");
                PreparedStatement ps = Con.prepareStatement(Query);
                ps.setInt(1, Integer.valueOf(NewPinTxt.getText()));
                ps.setInt(2, MyAccNum);
                if (ps.executeUpdate() == 1) {
                    JOptionPane.showMessageDialog(this, "PIN Updated");
                    new LOGIN().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Missing Information");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_ChangeBtnMouseClicked

